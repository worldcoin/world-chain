//! Test utilities for PBH, World Chain devnet fixtures, and the e2e harness.
//!
//! Public constants in this crate are consumed by downstream integration tests.
//! Internal harness constants stay `pub(crate)` so the chain-spec setup can share
//! them without exporting test-only protocol fixtures as part of the crate API.
#![cfg_attr(not(test), warn(unused_crate_dependencies))]

use revm_primitives::{Address, address};

/// Embedded genesis fixture used to build the e2e harness chain spec.
///
/// The fixture stays in JSON form so the harness can start from the same
/// baseline accounts and fork configuration as the devnet, then layer test-only
/// accounts on top before sealing the `OpChainSpec`.
pub(crate) const GENESIS: &str = include_str!("../res/genesis.json");

/// Optimism L1 block info predeploy.
///
/// Every Optimism block begins with a system deposit that calls this predeploy
/// to publish L1 attributes. The e2e harness synthesizes that deposit locally,
/// so the address is kept next to the chain-spec constants that define the
/// harness environment.
pub(crate) const L1_BLOCK_PREDEPLOY: Address = address!("4200000000000000000000000000000000000015");

/// System depositor used by the Optimism attributes deposit transaction.
///
/// This well-known sender is part of the Optimism protocol contract surface,
/// not a funded test account. It is only used when constructing the synthetic
/// L1 attributes deposit inserted as the first transaction of each block.
pub(crate) const SYSTEM_DEPOSITOR: Address = address!("DeaDDEaDDeAdDeAdDEAdDEaddeAddEAdDEAd0001");

/// Function selector for the Optimism L1 attributes call.
///
/// The selector matches the Ecotone-era L1 block attributes transaction used by
/// World Chain nodes. The harness currently fills the payload with zero values
/// because these tests only need a structurally valid system deposit. Keeping
/// the selector named avoids burying protocol calldata in the transaction
/// builder.
pub(crate) const SET_L1_BLOCK_SELECTOR: [u8; 4] = [0x44, 0x0a, 0x5e, 0x20];

/// Test-harness address for the `WorldIDAccountManager` proxy.
///
/// World Chain exposes account-manager behavior through the WIP-1001 precompile
/// address `0x...001d`. Preloading a proxy at the same address lets e2e tests
/// exercise calls through the production address without deploying contracts at
/// runtime.
pub(crate) const WORLD_ID_ACCOUNT_MANAGER: Address =
    address!("000000000000000000000000000000000000001d");

/// Test-harness address for `WorldIDAccountManagerImplV1`.
///
/// This address is written into the proxy's ERC-1967 implementation slot during
